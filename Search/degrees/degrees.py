def shortest_path(source, target):
    """
    Returns the shortest list of (movie_id, person_id) pairs
    that connect the source to the target.

    If no possible path, returns None.
    """
    print(f'Searching Connection between {people[source]["name"]} and {people[target]["name"]}')

    # Breadth First Search (BFS)
    queue = QueueFrontier()
    #parents = []

    # Create a visited node set
    visited = set()

    # for neighbor in neighbors_for_person(source):
    for movie_id, person_id in neighbors_for_person(source):
        # Person Id, It parent, Actions
        if not person_id == source:
            queue.add(Node(person_id, source, [(movie_id, person_id)]))

    #breakpoint()
    while not queue.empty():
        node = queue.remove()
        person_id, parent_id, actions = node.get_state(), node.get_parent(), node.get_actions()

        if node in visited:
            continue

        visited.add(node)

        if person_id == target:
            return actions
        
        # Ok
        for adj_movie, adj_person in neighbors_for_person(person_id):
            if not (adj_movie, adj_person) in visited and not adj_person == person_id:
                queue.add(Node(adj_person, person_id, actions + [(adj_movie, adj_person)]))
            
    return None


def person_id_for_name(name):
    """
    Returns the IMDB id for a person's name,
    resolving ambiguities as needed.
    """
    person_ids = list(names.get(name.lower(), set()))
    if len(person_ids) == 0:
        return None
    elif len(person_ids) > 1:
        print(f"Which '{name}'?")
        for person_id in person_ids:
            person = people[person_id]
            name = person["name"]
            birth = person["birth"]
            print(f"ID: {person_id}, Name: {name}, Birth: {birth}")
        try:
            person_id = input("Intended Person ID: ")
            if person_id in person_ids:
                return person_id
        except ValueError:
            pass
        return None
    else:
        return person_ids[0]


def neighbors_for_person(person_id):
    """
    Returns (movie_id, person_id) pairs for people
    who starred with a given person.
    """
    movie_ids = people[person_id]["movies"]
    neighbors = set()
    for movie_id in movie_ids:
        for person_id in movies[movie_id]["stars"]:
            neighbors.add((movie_id, person_id))
    return neighbors


